#include "libft.h"

char	*ft_strdup(char const *src)
{
	char	*str;
	int		i;

	if (src == NULL)
		return (NULL);
	str = malloc(ft_strlen(src) + 1);
	i = 0;
	if (str == 0)
		return (0);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
