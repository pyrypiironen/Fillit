#include "libft.h"

long int	ft_dstrlen(char const *str, char c)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (0);
	while (str[i] != '\0' && str[i] != c)
	{
		i++;
	}
	return (i);
}
